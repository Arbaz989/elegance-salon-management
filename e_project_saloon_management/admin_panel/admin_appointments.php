// Fetch appointments based on status
$statuses = ['pending', 'approved', 'rejected'];
$appointments = [];

foreach ($statuses as $status) {
    $appointments_query = "
        SELECT 
            a.id, 
            a.user_name, 
            a.user_email, 
            s.service_name, 
            a.appointment_date, 
            t.slot_time,
            u.user_name AS stylist_name 
        FROM appointments a
        JOIN time_slots t ON a.time_slot_id = t.id 
        JOIN services s ON a.service_id = s.id
        JOIN users u ON a.stylist_id = u.id
        WHERE a.status = '$status' AND u.role = 'stylist'";
    $appointments[$status] = $conn->query($appointments_query);
}

